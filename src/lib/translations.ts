// Helper function to get translated exercise name and description
export function getTranslatedExercise(
  exerciseName: string
) {
  const langTranslations = exerciseTranslations.uk
  const translation =
    langTranslations?.[
      exerciseName as keyof typeof langTranslations
    ]

  return {
    name: translation?.name || exerciseName,
    description: translation?.description || null,
  }
}

